# utils/keyboard.py

EN_RU_MAP = {
    # буквы
    'q': 'й', 'w': 'ц', 'e': 'у', 'r': 'к', 't': 'е',
    'y': 'н', 'u': 'г', 'i': 'ш', 'o': 'щ', 'p': 'з',
    '[': 'х', ']': 'ъ',

    'a': 'ф', 's': 'ы', 'd': 'в', 'f': 'а', 'g': 'п',
    'h': 'р', 'j': 'о', 'k': 'л', 'l': 'д', ';': 'ж', "'": 'э',

    'z': 'я', 'x': 'ч', 'c': 'с', 'v': 'м', 'b': 'и',
    'n': 'т', 'm': 'ь', ',': 'б', '.': 'ю', '/': '.',

    # верхний ряд
    '`': 'ё',

    # --- заглавные ---
    'Q': 'Й', 'W': 'Ц', 'E': 'У', 'R': 'К', 'T': 'Е',
    'Y': 'Н', 'U': 'Г', 'I': 'Ш', 'O': 'Щ', 'P': 'З',
    '{': 'Х', '}': 'Ъ',

    'A': 'Ф', 'S': 'Ы', 'D': 'В', 'F': 'А', 'G': 'П',
    'H': 'Р', 'J': 'О', 'K': 'Л', 'L': 'Д', ':': 'Ж', '"': 'Э',

    'Z': 'Я', 'X': 'Ч', 'C': 'С', 'V': 'М', 'B': 'И',
    'N': 'Т', 'M': 'Ь', '<': 'Б', '>': 'Ю', '?': ',',

    '~': 'Ё',
}

# инвертируем словарь для RU→EN
RU_EN_MAP = {v: k for k, v in EN_RU_MAP.items()}

EN2RU = str.maketrans(EN_RU_MAP)
RU2EN = str.maketrans(RU_EN_MAP)


def switch_layout(text: str) -> tuple[str, str, str]:
    """Вернёт (оригинал, en->ru, ru->en)."""
    t = (text or "").strip()
    if not t:
        return "", "", ""
    return t, t.translate(EN2RU), t.translate(RU2EN)
